def default_export_path(format="ods"):
    base = os.path.join(os.path.expanduser("~"), f"BoroughWaste_Plan.{format}")
    return base


def export_to_path(game, path):
    """Export to either ODS or XML based on file extension."""
    ext = os.path.splitext(path)[1].lower()
    if ext == ".ods":
        if not _has_pyexcel_ods():
            raise ImportError("pyexcel-ods is required for ODS export. Install it with: pip install pyexcel-ods")
        return export_to_ods_path(game, path)
    else:
        # Default to XML
        xml = build_workbook_xml(game)
        with open(path, "w", encoding="utf-8") as fh:
            fh.write(xml)
        return path


def import_from_path(game, path):
    """Import from either ODS or XML based on file extension."""
    ext = os.path.splitext(path)[1].lower()
    if ext == ".ods":
        if not _has_pyexcel_ods():
            raise ImportError("pyexcel-ods is required for ODS import. Install it with: pip install pyexcel-ods")
        return import_from_ods_path(game, path)
    else:
        with open(path, "r", encoding="utf-8") as fh:
            text = fh.read()
        sheets = _parse_xml(text)
        return apply_workbook(game, sheets)


