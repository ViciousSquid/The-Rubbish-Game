def _file_dialog(save, default_name="BoroughWaste_Plan.ods"):
    """Open a native open/save dialog via tkinter. Returns a path or None.
    Returns the sentinel 'NO_TK' if tkinter is unavailable."""
    try:
        import tkinter
        from tkinter import filedialog
    except Exception:
        return "NO_TK"
    try:
        root = tkinter.Tk()
        root.withdraw()
        root.attributes("-topmost", True)

        if save:
            # Default to ODS, but allow XML too
            opts = dict(
                defaultextension=".ods",
                filetypes=[
                    ("ODS Spreadsheet", "*.ods"),
                    ("XML Spreadsheet 2003", "*.xml"),
                    ("All files", "*.*")
                ],
                title="Save borough plan",
                initialfile=default_name,
            )
            path = filedialog.asksaveasfilename(**opts)
        else:
            opts = dict(
                filetypes=[
                    ("ODS Spreadsheet", "*.ods"),
                    ("XML Spreadsheet 2003", "*.xml"),
                    ("All files", "*.*")
                ],
                title="Import borough plan"
            )
            path = filedialog.askopenfilename(**opts)
        root.destroy()
        return path or None
    except Exception:
        return "NO_TK"


def prompt_export(game):
    """Returns (ok, message)."""
    path = _file_dialog(save=True)
    if path == "NO_TK":
        path = default_export_path("ods")
        try:
            export_to_path(game, path)
            return True, f"Saved to {path} (no file dialog available)."
        except Exception as exc:
            return False, f"Export failed: {exc}"
    if not path:
        return False, "Export cancelled."
    try:
        export_to_path(game, path)
        ext = os.path.splitext(path)[1].upper()
        return True, f"Exported plan to {os.path.basename(path)} ({ext} format)."
    except Exception as exc:
        return False, f"Export failed: {exc}"


def prompt_import(game):
    """Returns (ok, message)."""
    path = _file_dialog(save=False)
    if path == "NO_TK":
        # Try ODS first, then XML
        for ext in ["ods", "xml"]:
            path = default_export_path(ext)
            if os.path.exists(path):
                break
        else:
            return False, "No file dialog and no default plan to import."
    if not path:
        return False, "Import cancelled."
    if not os.path.exists(path):
        return False, "File not found."
    try:
        msg = import_from_path(game, path)
        ext = os.path.splitext(path)[1].upper()
        return True, f"{msg} ({ext} format)."
    except ET.ParseError as exc:
        return False, f"Could not parse file: {exc}"
    except Exception as exc:
        return False, f"Import failed: {exc}"
